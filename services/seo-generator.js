const DEFAULT_PLATFORM_URL = 'https://vibenote.studio';
const DEFAULT_THEME_COLOR = '#0a0d12';

function slugify(value = '') {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '') || 'client-website';
}

function escapeHtml(value = '') {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function stripHtml(value = '') {
  return String(value || '')
    .replace(/<[^>]*>/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function truncate(value = '', max = 160) {
  const clean = stripHtml(value);
  if (clean.length <= max) return clean;
  return `${clean.slice(0, max - 3).trim()}...`;
}

function normalizeUrl(url, fallback = DEFAULT_PLATFORM_URL) {
  const raw = String(url || '').trim();
  if (!raw) return fallback;
  if (/^https?:\/\//i.test(raw)) return raw.replace(/\/$/, '');
  return `https://${raw.replace(/^\/+/, '').replace(/\/$/, '')}`;
}

function inferBaseUrl(data = {}) {
  return normalizeUrl(
    data.canonical_url || data.website_url || data.domain || data.site_url || '',
    `https://${slugify(data.client_name || data.business_name || 'client-website')}.pages.dev`
  );
}

function normalizeList(value) {
  if (Array.isArray(value)) return value.map((item) => String(item).trim()).filter(Boolean);
  if (!value) return [];
  return String(value)
    .split(/\r?\n|,/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function pickBusinessName(data = {}) {
  return data.client_name || data.business_name || data.name || 'Business';
}

function pickSeoTitle(data = {}, page = {}) {
  const businessName = pickBusinessName(data);
  const location = data.location || data.address || '';
  const seoTitle = data.seo_title || '';
  const pageLabel = page.label || '';

  if (pageLabel && seoTitle) return `${seoTitle} | ${pageLabel}`;
  if (pageLabel) return `${businessName} | ${pageLabel}`;
  if (seoTitle) return seoTitle;
  if (location) return `${businessName} | ${location}`;
  return `${businessName} | Professional Website`;
}

function pickMetaDescription(data = {}, page = {}) {
  const custom = page.description || data.seo_description || data.description || data.about_business || '';
  const services = normalizeList(data.services);

  if (custom) return truncate(custom, 160);
  if (services.length) return truncate(`${pickBusinessName(data)} offers ${services.join(', ')}. Contact the team today for more information.`, 160);
  return truncate(`${pickBusinessName(data)} website generated by VibeNote with search-friendly structure and clear calls to action.`, 160);
}

function mapSchemaType(value = '') {
  const normalized = String(value || '').toLowerCase();
  if (normalized.includes('clinic') || normalized.includes('doctor') || normalized.includes('dental')) return 'MedicalBusiness';
  if (normalized.includes('law')) return 'LegalService';
  if (normalized.includes('restaurant') || normalized.includes('cafe')) return 'Restaurant';
  if (normalized.includes('salon') || normalized.includes('spa')) return 'BeautySalon';
  if (normalized.includes('gym') || normalized.includes('fitness')) return 'SportsActivityLocation';
  if (normalized.includes('real estate')) return 'RealEstateAgent';
  return 'LocalBusiness';
}

function generateLocalBusinessSchema(data = {}, options = {}) {
  const baseUrl = inferBaseUrl(data);
  const businessName = pickBusinessName(data);
  const services = normalizeList(data.services);
  const social = [data.facebook, data.instagram, data.linkedin, data.youtube, data.twitter, data.pinterest]
    .filter(Boolean)
    .map((item) => String(item).trim());

  return {
    '@context': 'https://schema.org',
    '@type': options.schemaType || mapSchemaType(data.schema_type || data.industry),
    name: businessName,
    url: options.pageUrl || baseUrl,
    email: data.email || data.contact_email || undefined,
    telephone: data.phone || data.whatsapp || undefined,
    description: pickMetaDescription(data, options.page || {}),
    address: data.address || data.location || undefined,
    areaServed: data.location || undefined,
    image: data.logo_base64 || undefined,
    logo: data.logo_base64 || undefined,
    sameAs: social.length ? social : undefined,
    knowsAbout: services.length ? services : undefined
  };
}

function generateMetaTags(businessData = {}, options = {}) {
  const baseUrl = normalizeUrl(options.baseUrl || inferBaseUrl(businessData));
  const pagePath = options.pagePath || '/';
  const canonicalUrl = normalizeUrl(options.canonicalUrl || `${baseUrl}${pagePath === '/' ? '' : pagePath}`);
  const title = pickSeoTitle(businessData, { label: options.pageTitle });
  const description = pickMetaDescription(businessData, { description: options.description });
  const ogImage = options.ogImage || businessData.logo_base64 || `${baseUrl}/logo.png`;

  return [
    `<title>${escapeHtml(title)}</title>`,
    `<meta name="description" content="${escapeHtml(description)}" />`,
    `<meta name="viewport" content="width=device-width, initial-scale=1" />`,
    `<link rel="canonical" href="${escapeHtml(canonicalUrl)}" />`,
    `<meta property="og:title" content="${escapeHtml(title)}" />`,
    `<meta property="og:description" content="${escapeHtml(description)}" />`,
    `<meta property="og:type" content="website" />`,
    `<meta property="og:url" content="${escapeHtml(canonicalUrl)}" />`,
    `<meta property="og:image" content="${escapeHtml(ogImage)}" />`,
    `<meta name="theme-color" content="${escapeHtml(options.themeColor || businessData.color_primary || DEFAULT_THEME_COLOR)}" />`
  ].join('\n');
}

function generateSitemap(pages = [], baseUrl = DEFAULT_PLATFORM_URL) {
  const root = normalizeUrl(baseUrl);
  const urls = pages.map((page) => {
    const loc = page.startsWith('http') ? page : `${root}${page.startsWith('/') ? page : `/${page}`}`;
    return `  <url><loc>${escapeHtml(loc)}</loc></url>`;
  }).join('\n');

  return `<?xml version="1.0" encoding="UTF-8"?>\n<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n${urls}\n</urlset>`;
}

function generateRobots(baseUrl = DEFAULT_PLATFORM_URL) {
  const root = normalizeUrl(baseUrl);
  return `User-agent: *\nAllow: /\n\nSitemap: ${root}/sitemap.xml\n`;
}

function ensureDoctype(html = '') {
  return /<!DOCTYPE html>/i.test(html) ? html : `<!DOCTYPE html>\n${html}`;
}

function ensureHtmlShell(html = '') {
  if (/<html[\s>]/i.test(html)) return html;
  return `<!DOCTYPE html>\n<html lang="en">\n<head>\n</head>\n<body>\n${html}\n</body>\n</html>`;
}

function ensureHead(html = '') {
  if (/<head[\s>]/i.test(html)) return html;
  return html.replace(/<html([^>]*)>/i, '<html$1>\n<head>\n</head>');
}

function stripExistingSeo(html = '') {
  return html
    .replace(/<title>[\s\S]*?<\/title>/i, '')
    .replace(/<meta[^>]+name="description"[^>]*>\s*/ig, '')
    .replace(/<meta[^>]+property="og:[^"]+"[^>]*>\s*/ig, '')
    .replace(/<meta[^>]+name="theme-color"[^>]*>\s*/ig, '')
    .replace(/<link[^>]+rel="canonical"[^>]*>\s*/ig, '')
    .replace(/<script type="application\/ld\+json" data-vibenote-schema>[\s\S]*?<\/script>\s*/ig, '')
    .replace(/<nav class="seo-site-links" data-vibenote-seo-links>[\s\S]*?<\/nav>\s*/ig, '')
    .replace(/<style data-vibenote-seo-links>[\s\S]*?<\/style>\s*/ig, '');
}

function ensureImageAltAttributes(html = '', businessName = 'Business') {
  let imageIndex = 0;
  return html.replace(/<img\b([^>]*?)>/ig, (match, attrs) => {
    if (/\balt\s*=\s*/i.test(attrs)) return match;
    imageIndex += 1;
    const alt = imageIndex === 1 ? `${businessName} logo` : `${businessName} website image ${imageIndex - 1}`;
    return `<img${attrs} alt="${escapeHtml(alt)}">`;
  });
}

function ensureHeadingHierarchy(html = '', businessName = 'Business') {
  if (/<h1\b/i.test(html)) return html;
  return html.replace(/<body([^>]*)>/i, `<body$1>\n<h1 style="position:absolute;left:-9999px;top:auto;width:1px;height:1px;overflow:hidden;">${escapeHtml(businessName)}</h1>`);
}

function buildInternalLinks() {
  return `
<nav class="seo-site-links" data-vibenote-seo-links aria-label="Website pages">
  <a href="index.html">Home</a>
  <a href="about.html">About</a>
  <a href="services.html">Services</a>
  <a href="contact.html">Contact</a>
</nav>
<style data-vibenote-seo-links>
.seo-site-links{display:flex;flex-wrap:wrap;gap:14px;justify-content:center;padding:18px 24px;margin:28px auto 0;max-width:960px;border-top:1px solid rgba(255,255,255,.12)}
.seo-site-links a{color:inherit;text-decoration:none;font-weight:600}
</style>`;
}

function decorateGeneratedHtml(html = '', businessData = {}, options = {}) {
  const businessName = pickBusinessName(businessData);
  const baseUrl = normalizeUrl(options.baseUrl || inferBaseUrl(businessData));
  const pagePath = options.pagePath || '/';
  const pageTitle = options.pageTitle || '';
  const pageUrl = `${baseUrl}${pagePath === '/' ? '' : pagePath}`;
  const schema = generateLocalBusinessSchema(businessData, {
    pageUrl,
    page: { description: options.description },
    schemaType: options.schemaType
  });

  let output = ensureDoctype(ensureHead(ensureHtmlShell(stripExistingSeo(html))));
  const metaTags = generateMetaTags(businessData, {
    baseUrl,
    pagePath,
    pageTitle,
    description: options.description,
    ogImage: options.ogImage
  });
  const schemaScript = `<script type="application/ld+json" data-vibenote-schema>${JSON.stringify(schema)}</script>`;

  output = output.replace(/<head([^>]*)>/i, `<head$1>\n${metaTags}\n${schemaScript}`);
  output = ensureImageAltAttributes(output, businessName);
  output = ensureHeadingHierarchy(output, businessName);

  if (/<\/body>/i.test(output)) {
    output = output.replace(/<\/body>/i, `${buildInternalLinks()}</body>`);
  } else {
    output += buildInternalLinks();
  }

  return output;
}

