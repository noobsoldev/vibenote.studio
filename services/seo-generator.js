function buildSupplementalPages(businessData = {}) {
  const businessName = pickBusinessName(businessData);
  const services = normalizeList(businessData.services);
  const logoMarkup = businessData.logo_base64
    ? `<img src="${businessData.logo_base64}" alt="${escapeHtml(businessName)} logo" class="brand-mark" loading="lazy">`
    : '';
  const aboutDescription = stripHtml(businessData.description || businessData.about_business || `Learn more about ${businessName}.`);
  const servicesMarkup = services.length
    ? services.map((service) => `<div class="card"><h3>${escapeHtml(service)}</h3><p class="muted">Professional delivery for ${escapeHtml(service.toLowerCase())}.</p></div>`).join('')
    : `<div class="card"><h3>Core services</h3><p class="muted">Service information will be updated here.</p></div>`;
  const contactItems = [
    businessData.phone ? `<div><strong>Phone:</strong> ${escapeHtml(businessData.phone)}</div>` : '',
    businessData.email ? `<div><strong>Email:</strong> ${escapeHtml(businessData.email)}</div>` : '',
    (businessData.address || businessData.location) ? `<div><strong>Address:</strong> ${escapeHtml(businessData.address || businessData.location)}</div>` : ''
  ].filter(Boolean).join('');
  const primaryCta = businessData.primary_cta || 'Get in touch';

  return [
    {
      path: 'about.html',
      contentType: 'text/html; charset=utf-8',
      content: buildPageShell({
        title: 'About',
        description: aboutDescription,
        businessData,
        pagePath: '/about.html',
        body: `<section class="hero"><div class="pill">About ${escapeHtml(businessName)}</div>${logoMarkup}<h1>${escapeHtml(businessName)}</h1><p>${escapeHtml(aboutDescription)}</p></section><section><div class="grid"><div class="card"><h2 class="section-title">Why clients choose us</h2><p class="muted">${escapeHtml(stripHtml(businessData.tagline || 'Focused service, clear communication, and fast delivery.'))}</p></div><div class="card"><h2 class="section-title">What we do</h2><p class="muted">${escapeHtml(services.length ? services.join(', ') : 'Our service details are available on the services page.')}</p></div></div><a class="cta" href="contact.html">${escapeHtml(primaryCta)}</a></section>`
      })
    },
    {
      path: 'services.html',
      contentType: 'text/html; charset=utf-8',
      content: buildPageShell({
        title: 'Services',
        description: services.length ? `${businessName} services include ${services.join(', ')}.` : `Services offered by ${businessName}.`,
        businessData,
        pagePath: '/services.html',
        body: `<section class="hero"><div class="pill">Services</div><h1>${escapeHtml(businessName)} Services</h1><p>${escapeHtml(stripHtml(businessData.service_descriptions || businessData.description || 'Explore our services and reach out for the right solution.'))}</p></section><section><div class="grid">${servicesMarkup}</div><a class="cta" href="contact.html">${escapeHtml(primaryCta)}</a></section>`
      })
    },
    {
      path: 'contact.html',
      contentType: 'text/html; charset=utf-8',
      content: buildPageShell({
        title: 'Contact',
        description: `Contact ${businessName} for ${services.length ? services.join(', ') : 'business enquiries'}.`,
        businessData,
        pagePath: '/contact.html',
        body: `<section class="hero"><div class="pill">Contact</div><h1>Contact ${escapeHtml(businessName)}</h1><p>${escapeHtml(stripHtml(businessData.cta_text || 'Use the details below to connect with our team.'))}</p></section><section><div class="card"><h2 class="section-title">Get in touch</h2><div class="contact-list">${contactItems || '<div class="muted">Contact details will be added here soon.</div>'}</div></div></section>`
      })
    }
  ];
}

function buildClientSiteFiles(businessData = {}, generatedHtml = '') {
  const baseUrl = inferBaseUrl(businessData);
  const pages = ['/', '/about.html', '/services.html', '/contact.html'];
  const indexHtml = decorateGeneratedHtml(generatedHtml, businessData, { baseUrl, pagePath: '/' });

  return [
    { path: 'index.html', content: indexHtml, contentType: 'text/html; charset=utf-8' },
    ...buildSupplementalPages(businessData),
    { path: 'sitemap.xml', content: generateSitemap(pages, baseUrl), contentType: 'application/xml; charset=utf-8' },
    { path: 'robots.txt', content: generateRobots(baseUrl), contentType: 'text/plain; charset=utf-8' }
  ];
}

